/**
 * Service de traduction UI sans librairie lourde.
 * Dictionnaire statique FR/EN, réactif via LanguageService.
 */
@Injectable({ providedIn: 'root' })
export class TranslationService {
  private readonly langService = inject(LanguageService);

  /**
   * Retourne la traduction pour la clé donnée dans la langue courante.
   * Si la clé n'existe pas, retourne la clé elle-même.
   */
  translate(key: string): string {
    const lang = this.langService.currentLang();
    return TRANSLATIONS[lang]?.[key] ?? key;
  }

  /** Signal computed du texte traduit — pour usage dans les templates avec `{{ tr() }}` */
  readonly t = computed(() => (key: string) => this.translate(key));
}
